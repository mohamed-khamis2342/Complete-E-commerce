using System;
using System.Collections.Generic;
using System.Text;

namespace E_commerce.Core.SharedDTO
{
    public class ApiResponseFroPagination<T>
    {
        // HTTP status code of the response.
        public int StatusCode { get; set; }

        // Indicates whether the request was successful.
        public bool Success { get; set; }
        public int PageNumber { get; set; }
        public int PageSize { get; set; }
      
        // Response data in case successful
        public T Data { get; set; }

        // List of error messages, if any.
        public List<string> Errors { get; set; }

        public ApiResponseFroPagination()
        {
            Success = true;
            Errors = new List<string>();
        }

        public ApiResponseFroPagination(int statusCode, T data)
        {
            StatusCode = statusCode;
            Success = true;
            Data = data;
            Errors = new List<string>();
        }

        public ApiResponseFroPagination(int statusCode, List<string> errors)
        {
            StatusCode = statusCode;
            Success = false;
            Errors = errors;
        }

        public ApiResponseFroPagination(int statusCode, string error)
        {
            StatusCode = statusCode;
            Success = false;
            Errors = new List<string> { error };
        }
    }
}
